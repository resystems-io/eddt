#!/usr/bin/env python3
"""Unit tests for markdown-index.py.

This script had zero existing test coverage before this file. Exercises
the alphabetical-index regenerator against synthesised markdown fixtures.
Checks:

* R01: terms are correctly paired with their most recent `## N.` section.
* R02: generated index entries are sorted alphabetically (case-insensitive).
* R03: an existing index block is replaced in place on re-run, not
  duplicated or appended.
* R04: generated entries use the documented reference-link + anchor shape.
* R05: content after the index block (e.g. a reference-links block
  appended by other tooling) is preserved.
* R06: exits non-zero when no ### headings are found.
* R07: second run on already-current content reproduces byte-identical
  output.
* R08: a ## N. Section / ### Term pair inside a fenced code block is not
  picked up as a real entry.
* R09: a ## N. Section / ### Term pair inside an HTML comment is not
  picked up as a real entry, while real entries before and after the
  comment are indexed correctly.
* R10: a same-line, self-closing comment does not block extraction of a
  real entry that follows it.
* R11: a legacy index block with no end marker is treated as extending
  to EOF when locating the existing block, and is fully replaced on
  regeneration.
* R12: in a document that also carries a TOC block and a Reference-links
  block (mirroring eddt-stakeholders.md's real shape), regenerating the
  index touches only the index block — the other two blocks pass
  through byte-for-byte unchanged.
* R13: when the committed index is already up-to-date, the script does
  not rewrite the file — mtime is preserved. This is a newly-added
  guarantee (this script previously always wrote unconditionally),
  matching markdown-toc.py/markdown-num.py's existing contract.

Run from the repo root or this directory:

    python3 designs/scripts/tests/test_markdown_index.py

Exits 0 on success, non-zero on any failed assertion.
"""

from __future__ import annotations

import os
import subprocess
import sys
import tempfile
import time
from pathlib import Path


HERE = Path(__file__).resolve().parent
SCRIPTS = HERE.parent
MD_INDEX = SCRIPTS / "markdown-index.py"


def _fail(msg: str) -> None:
    print(f"FAIL: {msg}", file=sys.stderr)
    sys.exit(1)


def _ok(msg: str) -> None:
    print(f"ok   {msg}")


def _run(path: Path) -> subprocess.CompletedProcess:
    """Invoke markdown-index.py against `path`; return the completed process."""
    return subprocess.run(
        ["python3", str(MD_INDEX), str(path)],
        capture_output=True,
        text=True,
    )


def _mktemp_md(suffix: str = ".md") -> Path:
    """Create a tempfile under SCRIPTS so snap-confined interpreters can see it."""
    fd, name = tempfile.mkstemp(suffix=suffix, dir=str(SCRIPTS))
    os.close(fd)
    return Path(name)


# ----------------------------------------------------------------------
# Fixtures
# ----------------------------------------------------------------------

# Basic glossary, no existing index. Covers: R01, R04.
DOC_SIMPLE = """\
# Glossary

## 1. General Terms

### Alpha

Definition of alpha.

### Beta

Definition of beta.

## 2. Advanced Terms

### Gamma

Definition of gamma.
"""

# Deliberately scrambled term order to prove alphabetical sorting. Covers: R02.
DOC_UNSORTED = """\
# Glossary

## 1. Terms

### Zeta

Def.

### Alpha

Def.

### Mid

Def.
"""

# Already has a valid index block with a stale entry set (missing "Beta",
# which is now present in the body). Covers: R03.
DOC_WITH_EXISTING_INDEX = """\
# Glossary

## 1. Terms

### Alpha

Def.

### Beta

Def.

---

## Alphabetical Index

<!-- Generated by scripts/markdown-index.py — re-run after adding or renaming entries. -->

- [Alpha][alpha] -- Terms

[alpha]: #alpha "Alpha"

<!-- /Alphabetical Index -->
"""

# Existing index block followed by trailing content from other tooling
# (simulating a refs-linkify reference-links block). Covers: R05.
DOC_WITH_TRAILING_CONTENT = """\
# Glossary

## 1. Terms

### Alpha

Def.

---

## Alphabetical Index

<!-- Generated by scripts/markdown-index.py — re-run after adding or renaming entries. -->

- [Alpha][alpha] -- Terms

[alpha]: #alpha "Alpha"

<!-- /Alphabetical Index -->

<!-- Reference links generated by scripts/refs-linkify.py -->

[d-01]: reify-refinements.md#d-01

<!-- /Reference links -->
"""

# No ### headings at all. Covers: R06.
DOC_NO_TERMS = """\
# Glossary

## 1. Terms

Just a section, no entries.
"""

# ## / ### -shaped lines inside a fenced code block. Covers: R08.
DOC_WITH_FENCE = """\
# Glossary

## 1. Real Section

### Real Term

Def.

```
## 9. Fake Section (inside fence, must be ignored)
### Fake Term (also inside fence)
```

### Another Real Term

Def.
"""

# ## / ### -shaped lines inside a leading HTML comment. Covers: R09.
DOC_WITH_COMMENT = """\
# Glossary

## 1. Real Section

### Real Term

Def.

<!--

## 9. Fake Section (inside comment, must be ignored)

### Fake Term (also inside comment)

-->

### Another Real Term

Def.
"""

# Same-line, self-closing comment immediately followed by a real entry.
# Covers: R10.
DOC_WITH_SAME_LINE_COMMENT = """\
# Glossary

<!-- a short note -->

## 1. Real Section

### Real Term

Def.
"""

# Legacy index block: heading present, but no end marker. Covers: R11.
DOC_WITH_LEGACY_INDEX = """\
# Glossary

## 1. Terms

### Alpha

Def.

### Beta

Def.

---

## Alphabetical Index

- [OldEntry][oldentry] -- Terms

[oldentry]: #oldentry "OldEntry"
"""

# Mirrors the real shape of eddt-stakeholders.md: a document carrying a
# TOC block and a Reference-links block alongside the Alphabetical Index
# block. Covers: R12.
DOC_WITH_OTHER_GENERATED_BLOCKS = """\
# Glossary

<!-- TOC generated by scripts/markdown-toc.py -->

- [1. Terms](#1-terms)

<!-- /TOC -->

## 1. Terms

### Alpha

Def.

### Beta

Def.

---

## Alphabetical Index

<!-- Generated by scripts/markdown-index.py — re-run after adding or renaming entries. -->

- [Alpha][alpha] -- Terms

[alpha]: #alpha "Alpha"

<!-- /Alphabetical Index -->

<!-- Reference links generated by scripts/refs-linkify.py -->

[d-01]: reify-refinements.md#d-01

<!-- /Reference links -->
"""


