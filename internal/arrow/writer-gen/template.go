var writerTemplate = template.Must(template.New("writer").Funcs(template.FuncMap{
	"dict": func(values ...any) map[string]any {
		m := make(map[string]any)
		for i := 0; i < len(values); i += 2 {
			m[values[i].(string)] = values[i+1]
		}
		return m
	},
}).Parse(writerTemplateStr))

// ImportInfo describes a single package import in the generated file.
type ImportInfo struct {
	Path  string // full import path (e.g. "myapp/entities")
	Name  string // base package name (e.g. "entities")
	Alias string // alias to use in generated code; empty means use Name
}

type templateData struct {
	PackageName string
	Imports     []ImportInfo
	Structs     []StructInfo
}

// Run executes the full generation pipeline: parse -> apply template -> write to file.
//
// If outPkgNameOverride is empty, the output package name is auto-detected from the first input package.
// If outPkgNameOverride is set and differs from a struct's origin package, the generated code will
// import that package and qualify struct type references accordingly.
//
// Package aliases are specified via PkgAliases in "importpath=alias" format, where importpath
// must be the full Go import path of the package (as resolved by packages.Load from the input
// directory). When an alias is provided, the generated import uses that alias and all type
// references are qualified with the alias instead of the base package name.
//
// External package types (e.g., netip.Addr, time.Time) that are NOT in the provided InputPkgs
// are handled through interface-based serialization with the following priority:
//  1. encoding.TextMarshaler — type is serialized to string via MarshalText()
//  2. fmt.Stringer — type is serialized to string via String()
//  3. encoding.BinaryMarshaler — type is serialized to binary via MarshalBinary()
//
// If the external type does not implement any of these interfaces, the field is skipped
// and a warning is emitted during generation.
func (g *Generator) Run(outPkgNameOverride string) error {
	// Parse pkg aliases early so bad input is caught before touching the filesystem.
	pkgAliases := map[string]string{}
	for _, a := range g.PkgAliases {
		parts := strings.SplitN(a, "=", 2)
		if len(parts) != 2 || parts[0] == "" || parts[1] == "" {
			return fmt.Errorf("invalid --pkg-alias %q: expected format 'original=replacement'", a)
		}
		pkgAliases[parts[0]] = parts[1]
	}

	parsedPkgName, _, structs, err := g.Parse()
	if err != nil {
		return err
	}

	if len(structs) == 0 {
		return fmt.Errorf("no target structs found matching specifications")
	}

	// Determine output package name.
	packageName := outPkgNameOverride
	if packageName == "" {
		packageName = parsedPkgName
		if packageName == "" {
			return fmt.Errorf("could not determine package name from input; use --pkg-name to specify one explicitly")
		}
	}

	// resolveAlias returns the alias for a given package import path, or "".
	// The key must be the full Go import path — no short-name or suffix matching.
	resolveAlias := func(pkgPath string) string {
		if alias, ok := pkgAliases[pkgPath]; ok {
			return alias
		}
		return ""
	}

	// Build the import map: collect unique packages that need to be imported.
	// A package needs importing when:
	//   - its name differs from the output package name, OR
	//   - an alias mapping explicitly targets it (forces import even if names match).
	type importKey = string // pkgPath
	importMap := map[importKey]ImportInfo{}
	for _, si := range structs {
		if si.PkgPath == "" {
			continue
		}
		if _, exists := importMap[si.PkgPath]; exists {
			continue
		}
		alias := resolveAlias(si.PkgPath)
		if si.PkgName != packageName || alias != "" {
			importMap[si.PkgPath] = ImportInfo{
				Path:  si.PkgPath,
				Name:  si.PkgName,
				Alias: alias,
			}
		}
	}

	// Convert import map to a sorted slice for deterministic output.
	imports := make([]ImportInfo, 0, len(importMap))
	for _, imp := range importMap {
		imports = append(imports, imp)
	}
	sort.Slice(imports, func(i, j int) bool { return imports[i].Path < imports[j].Path })

	// Validate that reserved Arrow import names don't collide with the output package or any alias.
	hardReserved := map[string]bool{"arrow": true, "array": true, "memory": true}
	for _, imp := range imports {
		effectiveName := imp.Name
		if imp.Alias != "" {
			effectiveName = imp.Alias
		}
		if hardReserved[effectiveName] {
			return fmt.Errorf("imported package alias/name %q collides with a required Arrow import; use --pkg-alias to choose a different alias", effectiveName)
		}
	}
	if hardReserved[packageName] {
		return fmt.Errorf("output package name %q collides with an import used in generated code; choose a different --pkg-name", packageName)
	}

	// Set Qualifier on each StructInfo based on whether it needs an import.
	for i := range structs {
		si := &structs[i]
		if imp, ok := importMap[si.PkgPath]; ok {
			qualifier := imp.Name
			if imp.Alias != "" {
				qualifier = imp.Alias
			}
			si.Qualifier = qualifier + "."
		}
		// else: struct is in the output package — no qualifier needed.
	}

	data := templateData{
		PackageName: packageName,
		Imports:     imports,
		Structs:     structs,
	}

	var buf bytes.Buffer
	if err := writerTemplate.Execute(&buf, data); err != nil {
		return fmt.Errorf("failed to execute template: %w", err)
	}

	// Format Source
	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return fmt.Errorf("failed to format generated source: %w\nSource:\n%s", err, buf.String())
	}

	// Write Output
	if err := os.WriteFile(g.OutPath, formatted, 0644); err != nil {
		return fmt.Errorf("failed to write output file: %w", err)
	}

	return nil
}
